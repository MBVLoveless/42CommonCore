#include <libft.h>

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	char				*d;
	char				*s;
	char				*temp;
	size_t				i;

	d = dest;
	s = src;
	i = 0;
	while (i < n)
	{
		temp[i] = s[i];
		i++;
	}
	i = 0;
	while (i < n)
	{
		d[i] = temp[i];
		i++;
	}
	return (dest);
}

